package br.com.TIO_lojao_informatica.entidades;

public class TipoTelefone {
	public int movel;
	public int residencial;
	public int comercial;

	public int getMovel() {
		return movel;
	}

	public void setMovel(int movel) {
		this.movel = movel;
	}

	public int getResidencial() {
		return residencial;
	}

	public void setResidencial(int residencial) {
		this.residencial = residencial;
	}

	public int getComercial() {
		return comercial;
	}

	public void setComercial(int comercial) {
		this.comercial = comercial;
	}

}
