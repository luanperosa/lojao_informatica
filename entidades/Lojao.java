package br.com.TIO_lojao_informatica.entidades;

public class Lojao {
	private Integer codigolj;
	private String nomelj;
	private String cnpj;
	private Integer codigotelefonelj;
	private Integer codigoendlj;
	private String email;
	
	public Integer getCodigolj() {
		return codigolj;
	}
	public void setCodigolj(Integer codigolj) {
		this.codigolj = codigolj;
	}
	public String getNomelj() {
		return nomelj;
	}
	public void setNomelj(String nomelj) {
		this.nomelj = nomelj;
	}
	public String getCnpj() {
		return cnpj;
	}
	public void setCnpj(String cnpj) {
		this.cnpj = cnpj;
	}
	public Integer getCodigotelefonelj() {
		return codigotelefonelj;
	}
	public void setCodigotelefonelj(Integer codigotelefonelj) {
		this.codigotelefonelj = codigotelefonelj;
	}
	public Integer getCodigoendlj() {
		return codigoendlj;
	}
	public void setCodigoendlj(Integer codigoendlj) {
		this.codigoendlj = codigoendlj;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	
	
}
