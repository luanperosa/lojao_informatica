package br.com.TIO_lojao_informatica.entidades;

public class TipodeEndereco {
	public String enderecoresidencial;
	public String enderecocomercial;
	public String enderecocorrespondencia;
	public String enderecocobranca;

	public String getEnderecoresidencial() {
		return enderecoresidencial;
	}

	public void setEnderecoresidencial(String enderecoresidencial) {
		this.enderecoresidencial = enderecoresidencial;
	}

	public String getEnderecocomercial() {
		return enderecocomercial;
	}

	public void setEnderecocomercial(String enderecocomercial) {
		this.enderecocomercial = enderecocomercial;
	}

	public String getEnderecocorrespondencia() {
		return enderecocorrespondencia;
	}

	public void setEnderecocorrespondencia(String enderecocorrespondencia) {
		this.enderecocorrespondencia = enderecocorrespondencia;
	}

	public String getEnderecocobranca() {
		return enderecocobranca;
	}

	public void setEnderecocobranca(String enderecocobranca) {
		this.enderecocobranca = enderecocobranca;
	}

}
